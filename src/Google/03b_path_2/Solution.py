def fill_route_map(map, start):
    route_map = [[None] * len(map[0]) for i in range(len(map))]
    route_map[start[0]][start[1]] = 1
    route = [start]
    while route:
        next_range = []
        for step in route:
            for i in [(0, 1), (0, -1), (1, 0), (-1, 0)]:
                dy = step[0] + i[0]
                dx = step[1] + i[1]
                if 0 <= dy < len(map) and 0 <= dx < len(map[0]):
                    if route_map[dy][dx] is None:
                        route_map[dy][dx] = route_map[step[0]][step[1]] + 1
                        if map[dy][dx] == 0:
                            next_range.append((dy, dx))
        route = next_range

    return route_map

